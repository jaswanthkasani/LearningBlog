class dataTypeCasting{
  public static void main(String []args){
    byte b;
    int i = 12;
    float f = 1.23;
    double d = 323.142;
    
    i = (int)d; // conversion from double to int
    b = (byte)d; // from double to byte
    System.out.println("d =" + d + "i = " + i);
    System.out.println("d =" + d + "b = " + b);
  }
}
                          
                          
                         
